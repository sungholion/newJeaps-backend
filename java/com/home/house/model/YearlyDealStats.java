package com.home.house.model;

public class YearlyDealStats {
    private String dealYear;
    private String maxDealAmount;
    private String minDealAmount;
    private String avgDealAmount;

    public String getDealYear() {
        return dealYear;
    }

    public void setDealYear(String dealYear) {
        this.dealYear = dealYear;
    }

    public String getMaxDealAmount() {
        return maxDealAmount;
    }

    public void setMaxDealAmount(String maxDealAmount) {
        this.maxDealAmount = maxDealAmount;
    }

    public String getMinDealAmount() {
        return minDealAmount;
    }

    public void setMinDealAmount(String minDealAmount) {
        this.minDealAmount = minDealAmount;
    }

    public String getAvgDealAmount() {
        return avgDealAmount;
    }

    public void setAvgDealAmount(String avgDealAmount) {
        this.avgDealAmount = avgDealAmount;
    }
    
}
